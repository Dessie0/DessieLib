package me.dessie.dessielib.particleapi.transform.orientation;

/**
 * Used in {@link Orientation} to rotate a specific amount.
 */
public enum Axis {
    /**
     * Rotates on the X Axis
     */
    X,

    /**
     * Rotates on the Y Axis
     */
    Y,

    /**
     * Rotates on the Z Axis
     */
    Z
}